package com.hans.hansrajcollege.models;

public class student_details {
    String sid,name,course,university_roll_no,email;

    public String getSid() {
        return sid;
    }

    public void setSid(String sid) {
        this.sid = sid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public String getUniversity_roll_no() {
        return university_roll_no;
    }

    public void setUniversity_roll_no(String university_roll_no) {
        this.university_roll_no = university_roll_no;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
